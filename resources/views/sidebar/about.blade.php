<h1>About</h1>
<p>
    This project uses citizen science waterbird observation data along with geographic information relevant to land use
    to demonstrate relative value of wetlands in the Greater Melbourne region.
</p>

<p>
    Urban wetlands collectively support waterbirds and other biodiversity assets. These contribute to healthy "green
    spaces" in cities. Some individual wetlands will support a greater diversity of species than others. But all
    wetlands are important and all have value as habitat for flora and fauna, for maintaining water quality, for helping
    with flood regulation and for human health and wellbeing.
</p>

<h2>Valuing Urban Wetlands project overview</h2>

<p>
    Wetlands in urban areas are increasingly recognised as a significantly under-appreciated biodiversity conservation
    asset. Urban wetlands are typically highly modified, small, and disconnected from the surface water network.
    Consequently, they are largely overlooked in habitat protection and urban planning: their value for species
    conservation in urban areas is often unknown.
</p>
<p>
    The AURIN-ALA High Impact Project Valuing Urban Wetlands project combines data drawn from citizen science
    observations and urban geography to provide simple reports on waterbird diversity and land use data for reporting on
    urban wetland value. Latham's Snipe (Gallinago hardwickii) is the focal species for metric development and forms the
    case study for this project.
</p>

<h2>
    Latham's Snipe
</h2>

<figure>
    <img src="{{ url('storage/photos/MarkLethlean_Lathams Snipe_Balnarring070119_resized.jpg') }}"
         title="Latham's Snipe - &copy; Mark Lethlean">
    <figcaption>Latham's Snipe - &copy; Mark Lethlean</figcaption>
</figure>

<p>
    A range of uncommon and threatened species are known to rely on urban wetlands. One such species is Latham's Snipe,
    which is a cryptic migratory shorebird that breeds in northern Japan and eastern Russia during the austral winter.
    It migrates to Australia in late winter, and it spends its non-breeding season in shallow, well-vegetated freshwater
    and estuarine wetlands where it builds up energy reserves for its return migration in autumn. As with all migratory
    shorebird species in the <a href="https://www.eaaflyway.net/migratory-waterbirds/" target="_blank">East Asian-Australasian
        Flyway</a>, its survival depends upon access to a range of habitats across its migratory lifecycle. In the case
    of Latham's Snipe, these habitats can often be found in urban areas or in areas converted to agricultural use. The
    species is threatened primarily by wetland habitat loss across its non-breeding range.
</p>
<p>
    Latham's Snipe is one of 37 migratory shorebird species listed as matters of national environmental significance
    under the <a href="https://www.dcceew.gov.au/environment/epbc" target="_blank">Environment Protection and Biodiversity Conservation
        Act 1999 (EPBC Act)</a>. At the global level, it is listed as <a
        href="https://www.iucnredlist.org/species/22693078/209318491" target="_blank">Near Threatened under the IUCN Red List </a> of
    species (BirdLife International, 2023), in recognition that it is potentially a declining species, but that
    information is lacking to determine trends and guide a management response.
</p>

<p>
    The <a href="https://lathamssnipeproject.wordpress.com/" target="_blank">Latham's Snipe Project</a> was established in 2015 to fill
    critical knowledge gaps about the ecology and management of this species, to improve conservation outcomes. This is
    a national scale citizen science program that also includes amateur and professional ornithologists. A key finding
    of monitoring to date is relatively common occurrence of snipe in urban wetlands, often in numbers exceeding the <a
        href="https://www.dcceew.gov.au/environment/epbc/publications/shorebirds-guidelines" target="_blank">EPBC Act threshold for
        national importance of 18 birds</a>. Many of these wetlands have no formal protection and every year, some get
    lost to land development and land use change.
</p>

