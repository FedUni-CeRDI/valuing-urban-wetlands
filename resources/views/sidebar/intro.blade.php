<h2>About</h2>
<p>
    This project uses citizen science waterbird observation data along with geographic information relevant to land use to demonstrate relative value of wetlands in the Greater Melbourne
    region.
</p>
<p>Urban wetlands collectively support waterbirds and other biodiversity assets. These contribute to healthy "green spaces" in cities. Some individual wetlands will support a greater
    diversity of species than others. But all wetlands are important and all have value as habitat for flora and fauna, for maintaining water quality, for helping with flood regulation and
    for human health and wellbeing.</p>
