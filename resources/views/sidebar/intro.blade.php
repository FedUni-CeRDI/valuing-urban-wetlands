<h2>About</h2>
<p>
    This project uses citizen science waterbird observation data along with geographic information relevant to land use
    to demonstrate relative value of wetlands in the Greater Melbourne
    region.
</p>
<p>Urban wetlands collectively support waterbirds and other biodiversity assets. These contribute to healthy "green
    spaces" in cities. Some individual wetlands will support a greater
    diversity of species than others. But all wetlands are important, and all have value as habitat for flora and fauna,
    for maintaining water quality, for helping with flood regulation and
    for human health and wellbeing.</p>
<p>
    Use this tool to learn more about your local wetland.
</p>
<p>
    <a href="#about">Read more..</a>
</p>

<h2>How to use this tool</h2>
<h3>To locate your wetland:</h3>
<p>
    Enter a wetland name in the search box OR<br>
    Use the mouse to pan and zoom to the location
</p>
<p>
    You can filter wetland types by Protected area status
</p>

<h3>
    To generate a wetland values report:
</h3>
<p>
    Navigate to the wetland of interest and click on wetland to get a report<br>
    The report queries all citizen science records and geographic information within 350m of the wetland edge
</p>
<p>
    <a href="https://inaturalist.ala.org.au/" target="_blank">Share your wetland fauna and flora observations with the Atlas of Living
        Australia</a>
</p>
